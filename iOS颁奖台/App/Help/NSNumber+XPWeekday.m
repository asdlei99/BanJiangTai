#import "NSNumber+XPWeekday.h"

@implementation NSNumber (XPWeekday)

//*  [1 - Sunday]
//*  [2 - Monday]
//*  [3 - Tuerday]
//*  [4 - Wednesday]
//*  [5 - Thursday]
//*  [6 - Friday]
//*  [7 - Saturday]
- (NSString *)shortWeekday
{
    switch(self.integerValue) {
        case 1: {
            return @"周日";
        }
            break;
            
        case 2: {
            return @"周一";
        }
            break;
            
        case 3: {
            return @"周二";
        }
            break;
            
        case 4: {
            return @"周三";
        }
            break;
            
        case 5: {
            return @"周四";
        }
            break;
            
        case 6: {
            return @"周五";
        }
            
        case 7: {
            return @"周六";
        }
            break;
            
        default: {
        }
            break;
    }
    return nil;
}

- (NSString *)weekday
{
    switch(self.integerValue) {
        case 1: {
            return @"星期日";
        }
            break;
            
        case 2: {
            return @"星期一";
        }
            break;
            
        case 3: {
            return @"星期二";
        }
            break;
            
        case 4: {
            return @"星期三";
        }
            break;
            
        case 5: {
            return @"星期四";
        }
            break;
            
        case 6: {
            return @"星期五";
        }
            
        case 7: {
            return @"星期六";
        }
            
        default: {
        }
            break;
    }
    return nil;
}

@end
